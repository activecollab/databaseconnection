declare(strict_types=1);

namespace ActiveCollab\DatabaseConnection\Spatial\Coordinate;

class Coordinate implements CoordinateInterface
{
    private float $value;

    public function __construct(float $value)
    {
        $this->value = $value;
    }

    public function getValue(): float
    {
        return $this->value;
    }
}
